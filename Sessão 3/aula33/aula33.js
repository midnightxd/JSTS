//Aula For-classíco (strutura de repetição) obs: i - index

initCode = () => {
    repaet = () => {
        const fruits = ['apple', 'banana', 'papaya'];

        for (let i=0; i < fruits.length; i++) {
            console.log(`Index: ${i} ->`, fruits[i]);
        }
    }
    repaet();
}
initCode();
